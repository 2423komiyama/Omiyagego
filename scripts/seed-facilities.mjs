async function main() {
  const conn = await mysql.createConnection(process.env.DATABASE_URL);
  
  console.log(`${FACILITIES.length}件の施設データを登録します...`);
  
  let inserted = 0;
  let updated = 0;
  
  for (const f of FACILITIES) {
    const now = new Date();
    // 既存チェック
    const [existing] = await conn.query('SELECT id FROM facilities WHERE id = ?', [f.id]);
    
    if (existing.length > 0) {
      // 更新
      await conn.query(`
        UPDATE facilities SET
          name = ?, shortLabel = ?, region = ?, prefecture = ?,
          latitude = ?, longitude = ?, insideGate = ?,
          updatedAt = ?
        WHERE id = ?
      `, [f.name, f.shortLabel, f.region, f.prefecture,
          f.latitude, f.longitude, f.insideGate ? 1 : 0,
          now, f.id]);
      updated++;
    } else {
      // 新規登録
      await conn.query(`
        INSERT INTO facilities (id, name, shortLabel, region, prefecture, latitude, longitude, insideGate, createdAt, updatedAt)
        VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
      `, [f.id, f.name, f.shortLabel, f.region, f.prefecture,
          f.latitude, f.longitude, f.insideGate ? 1 : 0,
          now, now]);
      inserted++;
    }
    console.log(`✓ ${f.name} (${f.id})`);
  }
  
  const [count] = await conn.query('SELECT COUNT(*) as cnt FROM facilities');
  console.log(`\n完了: 新規${inserted}件 / 更新${updated}件 / 合計${count[0].cnt}件`);
  
  await conn.end();
}

main().catch(console.error);
